package com.cubbysulotions.cliurches.Utilities;

public class UserPamisa {

    private String id;
    private String userID;
    private String parish;
    private String receiptStamp;
    private String recipient;
    private String forwhom;
    private String comment;
    private String date;
    private String time;
    private String type;
    private String status;

    public UserPamisa() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getParish() {
        return parish;
    }

    public void setParish(String parish) {
        this.parish = parish;
    }

    public String getReceiptStamp() {
        return receiptStamp;
    }

    public void setReceiptStamp(String receiptStamp) {
        this.receiptStamp = receiptStamp;
    }

    public String getRecipient() {
        return recipient;
    }

    public void setRecipient(String recipient) {
        this.recipient = recipient;
    }

    public String getForwhom() {
        return forwhom;
    }

    public void setForwhom(String forwhom) {
        this.forwhom = forwhom;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
